# Copy standard for missing block chars
for char in STANDARD:
    if char not in BLOCK:
        BLOCK[char] = STANDARD[char]

STYLES = {
    'standard': STANDARD,
    'block': BLOCK,
}


def render_text(text: str, style: str = 'standard') -> str:
    """Render text as ASCII art banner."""
    text = text.upper()
    font = STYLES.get(style, STANDARD)

    lines = [[] for _ in range(5)]

    for char in text:
        char_art = font.get(char, font.get(' '))
        for i, line in enumerate(char_art):
            lines[i].append(line)

    return '\n'.join(' '.join(row) for row in lines)


def main():
    parser = argparse.ArgumentParser(description="Generate ASCII art text banners")
    parser.add_argument("text", help="Text to convert to ASCII art")
    parser.add_argument("--style", choices=['standard', 'block'], default='standard',
                        help="Banner style (default: standard)")
    parser.add_argument("--width", type=int, default=80,
                        help="Max width before wrapping (default: 80)")
    args = parser.parse_args()

    print(render_text(args.text, args.style))


if __name__ == "__main__":
    main()
